# CTE

-- Q25.CTE to calculate the average salary and display employees earning above that average
WITH AVG_SALARY AS
(
SELECT AVG(SALARY) AS AVERAGE_SALARY FROM EMPLOYEES
)
SELECT EMPLOYEE_ID,
	EMPLOYEE_NAME,
    SALARY FROM EMPLOYEES
WHERE SALARY>(SELECT AVERAGE_SALARY FROM AVG_SALARY); 


# WINDOW FUNCTIONS

-- Q26.Rank employees by salary
SELECT EMPLOYEE_NAME,
SALARY,
RANK() OVER(ORDER BY SALARY DESC
) AS EMPLOYEE_RANK
FROM EMPLOYEES;

-- Q27.Top 3 highest-paid employees
WITH HIGH_PAID AS
(
SELECT EMPLOYEE_NAME,
SALARY,
RANK() OVER(ORDER BY SALARY DESC
) AS EMPLOYEE_SALARY_RANK
FROM EMPLOYEES
)
SELECT * FROM HIGH_PAID WHERE EMPLOYEE_SALARY_RANK<=3;

-- Q28.Assign row numbers based on salary
WITH EMPLOYEE_SALARY AS
(
SELECT EMPLOYEE_NAME,SALARY,
ROW_NUMBER() OVER(ORDER BY SALARY DESC
) AS SALARY_ROW_N
FROM EMPLOYEES
)
SELECT * FROM EMPLOYEE_SALARY;

-- Q29.Previous employee's salary using LAG()
WITH EMPLOYEE_SALARY AS
(
SELECT EMPLOYEE_NAME,SALARY,
LAG(SALARY) OVER(ORDER BY SALARY DESC
) AS PREVIOUS_SALARY
FROM EMPLOYEES
)
SELECT * FROM EMPLOYEE_SALARY;

-- Q30.Next employee's salary using LEAD()
WITH EMPLOYEE_SALARY AS
(
SELECT EMPLOYEE_NAME,SALARY,
LEAD(SALARY) OVER(ORDER BY SALARY DESC
) AS NEXT_SALARY
FROM EMPLOYEES
)
SELECT * FROM EMPLOYEE_SALARY;

-- Q31.Highest-paid employee in each department using a window function.
WITH EMP_SAL AS
(
SELECT EMPLOYEE_NAME,DEPARTMENT_NAME,SALARY,
RANK() OVER(PARTITION BY DEPARTMENT_NAME ORDER BY SALARY DESC
) AS HIGH_PAID
FROM DEPARTMENTS INNER JOIN EMPLOYEES ON DEPARTMENTS.DEPARTMENT_ID=EMPLOYEES.DEPARTMENT_ID
)
SELECT * FROM EMP_SAL WHERE HIGH_PAID=1;


