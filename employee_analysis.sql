# CASE

-- Q17.Classify salary
SELECT EMPLOYEE_ID,
EMPLOYEE_NAME,SALARY,
CASE
	WHEN SALARY>=60000 THEN 'HIGH'
	WHEN SALARY>=45000 THEN 'MEDIUM'
	ELSE 'LOW'
END AS CATEGORY
FROM EMPLOYEES;

-- Q18.Classify performance
SELECT EMPLOYEE_ID,
	PERFORMANCE_SCORE,
	CASE
		WHEN PERFORMANCE_SCORE>=90 THEN 'EXCELLENT'
		WHEN PERFORMANCE_SCORE>=80 THEN 'GOOD'
		WHEN PERFORMANCE_SCORE>=70 THEN 'AVERAGE'
		ELSE 'NEEDS IMPROVEMENT'
END AS RATING
FROM PERFORMANCE;

-- Q19.Bonus eligibility
SELECT BONUS,
CASE
	WHEN BONUS>=15000 THEN 'ELIGIBLE'
    ELSE 'NOT ELIGIBLE'
END AS BONUS_STATUS
FROM PERFORMANCE;

